# conditions, Loops, Logical operators

# operators
# equals/not to
print(True == True) # outputs True
print(False == True) # outputs False
print(False == False) # ...
print(4 == 4) 
print(4 != 4)

# greater/less/and equal then
print(5 > 6) 
print(2 < 2) 
print(2 <= 2) 
print(5 >= 4)
# with strings
print(len('two') < len('three')) 

# AND OR operators
print(True and False)
print(True or False)
print(12 or 0);
print('Ali' and 'Sawari')